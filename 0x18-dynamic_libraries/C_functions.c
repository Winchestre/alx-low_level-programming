#include <stdio.h>

/**
 * add - adds two integer inputs
 * @a: First integer argument
 * @b: Second integer argument
 *
 * Return: a + b
 */
int add(int a, int b)
{
	return (a + b);
}

/**
 * sub: Subtraction function
 * @a: First argument
 * @b: Second argument
 *
 * Return: a - b
 */
int sub(int a, int b)
{
	return (a - b);
}

/**
 * mul - Multiplication function
 * @a: First argument
 * @b: Second argument
 *
 * Return: a * b
 */
int mul(int a, int b)
{
	return (a * b);
}

/**
 * div - Division function
 * @a: First argument
 * @b: Second argument
 *
 * Return: a / b
 */
int div(int a, int b)
{
	return (a / b);
}

/**
 * mod - Moduli function
 * @a: First argument
 * @b: Second argument
 *
 * Return: a % b
 */
int mod(int a, int b)
{
	return (a % b);
}
